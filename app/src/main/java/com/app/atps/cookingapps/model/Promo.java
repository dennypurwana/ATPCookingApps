public class Promo {
    private String idPromo;
    private String promo;
    private String promoDetail;
    private String imagePromo;

    public String getIdPromo() {
        return idPromo;
    }

    public void setIdPromo(String idPromo) {
        this.idPromo = idPromo;
    }

    public String getPromo() {
        return promo;
    }

    public void setPromo(String promo) {
        this.promo = promo;
    }

    public String getPromoDetail() {
        return promoDetail;
    }

    public void setPromoDetail(String promoDetail) {
        this.promoDetail = promoDetail;
    }

    public String getImagePromo() {
        return imagePromo;
    }

    public void setImagePromo(String imagePromo) {
        this.imagePromo = imagePromo;
    }



}
